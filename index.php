    <h1>lazy-simon.js</h1>
    <p>Minimal effort 350 byte JavaScript library to lazy load all &lt;img&gt; on your website</p>
   <p>Lazy loading the images on your websites is a good idea in 99% of the cases. As all the other lazy load libraries where way to heavy for use case I build lazy-simon.js.</p>
   <p>As it is <b>only 350 byte in total size</b>, I now can include it on every website I develop without having to worry about performance impacts. Only upsides here ðŸ¤©</p>
 
 <h2>Usage</h2>
   <p>Just include the lazy-simon.js  script at the bottom of your body.</p>
    <pre><span style='color:#d2cd86; '>&lt;</span>script src<span style='color:#d2cd86; '>=</span><span style='color:#02d045; '>"</span><span style='color:#00c4c4; '>https://lazy.simon-frey.eu/js/lazy_simon.min.js</span><span style='color:#02d045; '>"</span><span style='color:#d2cd86; '>></span><span style='color:#d2cd86; '>&lt;</span><span style='color:#d2cd86; '>/</span>script<span style='color:#d2cd86; '>></span></pre>
    <h2>Technical info</h2>
    <p>The script uses a <i>Intersection Observer</i> for the getting the elements that are currently viewed. To check what browser all support this feauture checkout: <a href="https://caniuse.com/#search=intersectionobserver">https://caniuse.com/#search=intersectionobserver</a></p>
    <p>You can find the unminified version at <a href="https://lazy.simon-frey.eu/js/lazy_simon.min.js">https://lazy.simon-frey.eu/js/lazy_simon.min.js</a> and the script is licensed <a href="mit.txt">MIT</a></p>

